export class TimeT3 {
    constructo(a, k = 1.16, b = 1875) {
        this.a = a;
        this.k = k;
        this.b = b;
    }
    valueOf(lvl) {
        return this.a * Math.pow(this.k, lvl-1) - this.b;
    }
};
